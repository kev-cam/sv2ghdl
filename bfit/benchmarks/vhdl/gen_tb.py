#!/usr/bin/env python3
# Generate a self-checking, LFSR-driven testbench for an ITC'99-style VHDL
# circuit whose ports are all `bit` / `bit_vector`. The DUT is clocked for
# CYCLES cycles with pseudo-random inputs; every output bit is folded into a
# 64-bit checksum that is printed at the end. Same stimulus on every simulator
# => the checksum must match across nvc / ghdl (a correctness gate before timing).
#
# usage: gen_tb.py <circuit.vhd> <entity> > <entity>_tb.vhd
import re, sys

def load(path):
    return open(path, encoding="latin-1").read()

def entity_ports(txt, ent):
    # grab `entity ENT is port( ... );` (first ');' that closes the port clause)
    m = re.search(r"entity\s+%s\s+is\b" % re.escape(ent), txt, re.I)
    body = txt[m.end():]
    pm = re.search(r"port\s*\((.*)", body, re.I | re.S)
    s = pm.group(1)
    # find matching close paren for the port(
    depth, end = 1, 0
    for i, c in enumerate(s):
        if c == "(": depth += 1
        elif c == ")":
            depth -= 1
            if depth == 0: end = i; break
    return s[:end]

def parse(clause):
    ports = []
    for decl in clause.split(";"):
        decl = decl.strip()
        if not decl or ":" not in decl: continue
        names, rest = decl.split(":", 1)
        rest = rest.strip()
        mo = re.match(r"(in|out|inout)\s+(.*)", rest, re.I)
        if not mo:  # no direction (shouldn't happen for a port) -> skip
            continue
        direction, typ = mo.group(1).lower(), mo.group(2).strip()
        vm = re.match(r"bit_vector\s*\(\s*(\d+)\s+(downto|to)\s+(\d+)\s*\)", typ, re.I)
        if vm:
            hi, lo = int(vm.group(1)), int(vm.group(3))
            if vm.group(2).lower() == "to": hi, lo = lo, hi
            width = hi - lo + 1
            kind = ("vec", hi, lo, width)
        elif re.match(r"bit\b", typ, re.I):
            kind = ("bit",)
        elif re.match(r"integer\b", typ, re.I):
            # `integer` or `integer range <expr> downto <expr>`. Keep the range
            # text verbatim: a TB signal associated with a constrained formal
            # must have the SAME range (ghdl enforces this; nvc is lenient), and
            # the range is an expression (e.g. 2**20-1) we needn't evaluate --
            # driving/clamping uses the signal's own 'low/'high attributes.
            rm = re.match(r"integer\s+range\s+(.*)$", typ, re.I | re.S)
            kind = ("int", rm.group(1).strip() if rm else None)
        else:
            raise ValueError("unsupported type '%s' in: %s" % (typ, decl))
        for nm in names.split(","):
            ports.append((nm.strip(), direction, kind))
    return ports

def main():
    path, ent = sys.argv[1], sys.argv[2]
    ports = parse(entity_ports(load(path), ent))
    clk = next((p[0] for p in ports if p[0].lower() in ("clock", "clk")), None)
    rst = next((p[0] for p in ports if p[0].lower() in ("reset", "rst")), None)
    ins  = [p for p in ports if p[1] == "in"  and p[0] not in (clk, rst)]
    outs = [p for p in ports if p[1] == "out"]

    L = []
    w = L.append
    w("-- AUTO-GENERATED by gen_tb.py -- self-checking LFSR benchmark wrapper")
    w("library ieee; use ieee.std_logic_1164.all; use ieee.numeric_std.all;")
    w("use std.env.stop; use std.textio.all;")
    # RESTART: the ITC'99 cores are controllers that reach a halt state and stop
    # toggling; from there the sim does no work and the checksum goes periodic
    # (b17 gave the *same* checksum at 10k and 20k cycles). Re-pulsing reset
    # every RESTART cycles keeps the DUT executing for the whole run, so the
    # benchmark measures RTL activity and the checksum stays cycle-sensitive.
    w("entity %s_tb is generic (CYCLES : natural := 2000000;" % ent)
    w("                        RESTART : natural := 512); end entity;")
    w("architecture bench of %s_tb is" % ent)
    w("  signal clk : bit := '0';")
    if rst: w("  signal rst : bit := '1';")
    # Initialise every signal. VHDL `integer` defaults to INTEGER'LEFT
    # (-2147483648), and a DUT that reads an input before the first drive lands
    # (one delta later) can then overflow on e.g. `IR := -IR` -- b14 does
    # exactly this. bit/bit_vector default to '0' but are initialised for
    # symmetry and reproducibility.
    def sigdecl(k):
        if k[0] == "bit": return "bit := '0'"
        if k[0] == "int":
            return "integer range %s := 0" % k[1] if k[1] else "integer := 0"
        return "bit_vector(%d downto %d) := (others => '0')" % (k[1], k[2])
    for nm, _, k in ins:
        w("  signal %s : %s;" % (nm, sigdecl(k)))
    for nm, _, k in outs:
        w("  signal %s : %s;" % (nm, sigdecl(k)))
    w("  signal done : boolean := false;")
    w("  -- fold a bit_vector into a rotating 64-bit checksum")
    w("  function fold(acc : unsigned(63 downto 0); v : bit_vector) return unsigned is")
    w("    variable r : unsigned(63 downto 0) := acc;")
    w("  begin")
    w("    for i in v'range loop")
    w("      r := r(62 downto 0) & r(63);")
    w("      if v(i) = '1' then r(0) := not r(0); end if;")
    w("    end loop; return r;")
    w("  end function;")
    w("  function fold1(acc : unsigned(63 downto 0); b : bit) return unsigned is")
    w("    variable r : unsigned(63 downto 0) := acc(62 downto 0) & acc(63);")
    w("  begin if b = '1' then r(0) := not r(0); end if; return r; end function;")
    w("  function foldi(acc : unsigned(63 downto 0); v : integer) return unsigned is")
    w("    variable r : unsigned(63 downto 0) := acc(62 downto 0) & acc(63);")
    w("  begin return r xor to_unsigned(v mod 65536, 64); end function;")
    w("begin")
    # DUT instance (named association)
    conns = ["%s => clk" % clk] if clk else []
    if rst: conns.append("%s => rst" % rst)
    conns += ["%s => %s" % (nm, nm) for nm, _, _ in ins + outs]
    w("  dut: entity work.%s port map (%s);" % (ent, ", ".join(conns)))
    w("  clkgen: process begin")
    w("    while not done loop clk <= '0'; wait for 5 ns; clk <= '1'; wait for 5 ns; end loop; wait;")
    w("  end process;")
    w("  main: process")
    w("    variable lfsr : unsigned(31 downto 0) := to_unsigned(305419896, 32);")
    w("    variable chk  : unsigned(63 downto 0) := (others => '0');")
    w("    variable fb   : std_logic; variable l : line;")
    w("  begin")
    if rst:
        w("    rst <= '1'; for k in 1 to 4 loop wait until clk = '1'; end loop; rst <= '0';")
    w("    for c in 1 to CYCLES loop")
    w("      wait until clk = '1';")
    if rst:
        w("      if RESTART > 0 then")
        w("        if c mod RESTART = 0 then rst <= '1';")
        w("        elsif c mod RESTART = 2 then rst <= '0'; end if;")
        w("      end if;")
    # drive inputs from LFSR (advance one step per bit consumed)
    for nm, _, k in ins:
        n = 1 if k[0] in ("bit", "int") else k[3]
        for _ in range(n):
            w("      fb := lfsr(31) xor lfsr(21) xor lfsr(1) xor lfsr(0); lfsr := lfsr(30 downto 0) & fb;")
        if k[0]=="bit":
            w("      %s <= '1' when lfsr(0)='1' else '0';" % nm)
        elif k[0]=="int":
            if k[1]:   # constrained formal: stay inside its range
                w("      %s <= %s'low + (to_integer(lfsr(7 downto 0)) "
                  "mod (%s'high - %s'low + 1));" % (nm, nm, nm, nm))
            else:
                w("      %s <= to_integer(lfsr(7 downto 0));" % nm)
        else:
            w("      %s <= to_bitvector(std_logic_vector(lfsr(%d downto 0)));" % (nm, k[3]-1))
    # fold outputs into checksum
    for nm, _, k in outs:
        if k[0]=="bit":
            w("      chk := fold1(chk, %s);" % nm)
        elif k[0]=="int":
            w("      chk := foldi(chk, %s);" % nm)
        else:
            w("      chk := fold(chk, %s);" % nm)
    w("    end loop;")
    w("    write(l, string'(\"%s CHK=\")); hwrite(l, std_logic_vector(chk)); writeline(output, l);" % ent)
    w("    done <= true; stop;")
    w("  end process;")
    w("end architecture;")
    print("\n".join(L))

main()
